    {
        $this->setProperty('amount', $value);
        return $this;
    }

    /**
    * The start date and time of the item (in <a href="http://en.wikipedia.org/wiki/ISO_8601">ISO 8601 date format</a>).
    * @param  $value
    * @return $this
